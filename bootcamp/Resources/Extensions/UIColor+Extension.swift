import UIKit

extension UIColor {
    
    static let mainBackground: UIColor = UIColor(named: "mainBackground") ?? .white
    static let secondaryBackground: UIColor = UIColor(named: "secondaryBackground") ?? .lightGray
    static let mainAppColor: UIColor = UIColor(named: "mainAppColor") ?? .purple
    static let secondaryAppColor: UIColor = UIColor(named: "secondaryAppColor") ?? .purple
    static let mainLabel: UIColor = UIColor(named: "mainLabel") ?? .black
    static let secondaryLabel: UIColor = UIColor(named: "secondaryLabel") ?? .lightGray
    static let cancelButtonTitle: UIColor = UIColor(named: "cancelButtonTitle") ?? .white
    static let dividerGrey: UIColor = UIColor(named: "dividerGrey") ?? .lightGray
    static let errorBackground: UIColor = UIColor(named: "errorBackground") ?? .red
    static let errorTitle: UIColor = UIColor(named: "errorTitle") ?? .white
}
